<?php

// For 

//Var inicial//Condicional//Incrementa + 1 
for ($cont = 0; $cont <= 5; $cont++) { 
    echo "--- Contador é $cont ---" .PHP_EOL ;
    sleep(2);
    // adormece o terminal por dois segundos 
}

for ($cont = 5; $cont >= 0; $cont--) { 
    echo "--- Contador é $cont ---" .PHP_EOL ;
    sleep(2);
    // Função inversa 
}